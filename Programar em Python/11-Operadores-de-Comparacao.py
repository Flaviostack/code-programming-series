# Programar em Python #10 - Operadores de Comparação

'''
	== -> Igual
	!= -> Diferente
	> -> Maior
	< -> Menor
	>= -> Maior e igual
	<= -> Menor e igual

	and -> E
	or -> Ou
'''

x = 10;
y = 20;

if x <= y or x == y:
	print('Esta condição é verdadeira!');
else:
	print('A condição anterior é falsa!');