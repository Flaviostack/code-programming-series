# Programar em Python #06 - Mais sobre as Strings

'''
	\n -> Nova linha
	\t -> Nova tab (indentação)
	len(variable) -> Retorna o número de carateres (no caso da string)
	variable.upper() -> Transforma todos os carateres da string em letras maiúsculas
	variable.lower() -> Transforma todos os carateres da string em letras minúsculas
'''

string = "Eu sou\n\tuma string!";

print(len(string));
print(string.upper());
print(string.lower());